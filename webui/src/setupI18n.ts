const getInitialLanguage = () => {
  if (typeof window !== 'undefined') {
    const saved = window.localStorage.getItem('mcp.session.language') || window.localStorage.getItem('i18nextLng');
    if (saved === 'de' || saved === 'en') return saved;
  }
  return 'en';
};

